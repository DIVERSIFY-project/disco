package eu.diversify.disco.cloudml.transformations;

import static org.cloudml.core.builders.Commons.*;
import org.cloudml.core.DeploymentModel;

public class MdmsModelCreator {
  private DeploymentModel dm = null;
  
  public DeploymentModel create() {
      return aDeployment()
              .named("mdms")
              .withProvider(aProvider().named("provider"))
              .withNodeType(aNode()
                .named("EC2")
                .providedBy("provider"))
              .withArtefact(anArtefact()
                .named("MDMS")
                .withServerPort(aServerPort()
                    .named("content")
                    .remote())
                .withClientPort(aClientPort()
                    .named("db")
                    .remote()
                    .mandatory())
                .withClientPort(aClientPort()
                    .named("ringo")
                    .mandatory()
                    .local()))
              .withArtefact(anArtefact()
                .named("RingoJS")
                .withServerPort(aServerPort()
                    .named("ringo")
                    .local())
                .withClientPort(aClientPort()
                    .named("js")
                    .mandatory()
                    .local())
                .withClientPort(aClientPort()
                    .named("jvm")
                    .mandatory()
                    .local()))
              .withArtefact(anArtefact()
                .named("Rhino")
                .withServerPort(aServerPort()
                    .named("js")
                    .local())
                .withClientPort(aClientPort()
                    .named("jvm")
                    .mandatory()
                    .local()))
              .withArtefact(anArtefact()
                .named("OpenJDK")
                .withServerPort(aServerPort()
                    .named("jvm")
                    .local()))
              .withArtefact(anArtefact()
                .named("MySQL")
                .withServerPort(aServerPort()
                    .named("db")
                    .remote()))
              .withArtefact(anArtefact()
                .named("Balancer")
                .withClientPort(aClientPort()
                    .named("content")
                    .mandatory()
                    .remote()))
              .withBinding(aBinding()
                .named("Content")
                .from("Balancer", "content")
                .to("MDMS", "content"))
              .withBinding(aBinding()
                .named("DB")
                .from("MDMS", "db")
                .to("MySQL", "db"))
              .withBinding(aBinding()
                .named("Ringo")
                .from("MDMS", "ringo")
                .to("RingoJS", "ringo"))
              .withBinding(aBinding()
                .named("JS")
                .from("RingoJS", "js")
                .to("Rhino", "js"))
              .withBinding(aBinding()
                .named("Jvm-RingoJS")
                .from("RingoJS", "jvm")
                .to("OpenJDK", "jvm"))
              .withBinding(aBinding()
                .named("Jvm-Rhino")
                .from("Rhino", "jvm")
                .to("OpenJDK", "jvm"))
              .withNodeInstance(aNodeInstance()
                .named("ec2_bal")
                .ofType("EC2"))
              .withNodeInstance(aNodeInstance()
                .named("ec2_mdms")
                .ofType("EC2"))
              .withNodeInstance(aNodeInstance()
                .named("ec2_db")
                .ofType("EC2"))
              .withArtefactInstance(anArtefactInstance()
                .named("balancer")
                .ofType("Balancer")
                .hostedBy("ec2_bal"))
              .withArtefactInstance(anArtefactInstance()
                .named("mdms")
                .ofType("MDMS")
                .hostedBy("ec2_mdms"))
              .withArtefactInstance(anArtefactInstance()
                .named("ringoJS")
                .ofType("RingoJS")
                .hostedBy("ec2_mdms"))
              .withArtefactInstance(anArtefactInstance()
                .named("rhino")
                .ofType("Rhino")
                .hostedBy("ec2_mdms"))
              .withArtefactInstance(anArtefactInstance()
                .named("openJDK")
                .ofType("OpenJDK")
                .hostedBy("ec2_mdms"))
              .withArtefactInstance(anArtefactInstance()
                .named("mySQL")
                .ofType("MySQL")
                .hostedBy("ec2_db"))
              .withBindingInstance(aBindingInstance()
                .named("content")
                .ofType("Content")
                .from("balancer", "content")
                .to("mdms", "content"))              
              .withBindingInstance(aBindingInstance()
                .named("db")
                .ofType("DB")
                .from("mdms", "db")
                .to("mySQL", "db"))          
              .withBindingInstance(aBindingInstance()
                .named("ringo")
                .ofType("Ringo")
                .from("mdms", "ringo")
                .to("ringoJS", "ringo"))              
              .withBindingInstance(aBindingInstance()
                .named("js")
                .ofType("JS")
                .from("ringoJS", "js")
                .to("rhino", "js"))              
              .withBindingInstance(aBindingInstance()
                .named("jvm-ringoJS")
                .ofType("Jvm-RingoJS")
                .from("ringoJS", "jvm")
                .to("openJDK", "jvm"))              
              .withBindingInstance(aBindingInstance()
                .named("jvm-rhino")
                .ofType("Jvm-Rhino")
                .from("rhino", "jvm")
                .to("openJDK", "jvm"))              
              .build();

  }      

}
